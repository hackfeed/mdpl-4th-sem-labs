EXTRN TOUHEX : NEAR
EXTRN UHEX : BYTE

PUBLIC CRLF
PUBLIC OUTUHEX

; Data segment.
DATASEG SEGMENT PARA PUBLIC 'DATA'
    OUTMSG DB 'Unsigned hexadecimal number: $'
DATASEG ENDS

; Main code segment.
CODESEG SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG

CRLF PROC NEAR
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    RET
CRLF ENDP

OUTUHEX PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET OUTMSG
    INT 21H

    CALL TOUHEX

    MOV AH, 9
    MOV DX, OFFSET UHEX
    INT 21H

    CALL CRLF
    CALL CRLF

    RET
OUTUHEX ENDP

CODESEG ENDS
END