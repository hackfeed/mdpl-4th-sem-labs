EXTRN NUMBER : WORD

PUBLIC UHEX
PUBLIC SBIN

PUBLIC TOUHEX

; Data segment.
DATASEG SEGMENT PARA PUBLIC 'DATA'
    DIVISOR DB 16
    UHEX DB 4 DUP('0'), '$'
    SBIN DB 16 DUP('0'), '$'
DATASEG ENDS

CODESEG SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG, DS:DATASEG

TOUHEX PROC NEAR
    MOV AX, NUMBER
    MOV CX, 4
    MOV BX, 3
    GETHEX:
        DIV DIVISOR
        ADD AH, '0'
        MOV UHEX[BX], AH
        DEC BX
        LOOP GETHEX
    RET
TOUHEX ENDP

CODESEG ENDS
END